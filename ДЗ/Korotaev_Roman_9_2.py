import numpy as np
from scipy.stats import linregress

# –î–∞–Ω–Ω—ã–µ
x = np.array([38, 41, 24, 60, 41, 51, 58, 50, 65, 33])
y = np.array([73, 74, 43, 107, 65, 73, 99, 72, 100, 48])

# –í—ã—á–∏—Å–ª–µ–Ω–∏–µ –≤—ã–±–æ—Ä–æ—á–Ω–æ–≥–æ –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç–∞ –∫–æ—Ä—Ä–µ–ª—è—Ü–∏–∏
correlation_coefficient = np.corrcoef(x, y)[0, 1]

# –ü–æ—Å—Ç—Ä–æ–µ–Ω–∏–µ –ª–∏–Ω–µ–π–Ω–æ–π —Ä–µ–≥—Ä–µ—Å—Å–∏–∏ –∏ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ p-value
slope, intercept, r_value, p_value, std_err = linregress(x, y)

# –í—ã–≤–æ–¥ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
print(f"–í—ã–±–æ—Ä–æ—á–Ω—ã–π –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç –∫–æ—Ä—Ä–µ–ª—è—Ü–∏–∏: {correlation_coefficient:.3f}")
print(f"–£—Ä–∞–≤–Ω–µ–Ω–∏–µ –ª–∏–Ω–µ–π–Ω–æ–π —Ä–µ–≥—Ä–µ—Å—Å–∏–∏: y = {slope:.3f}x + {intercept:.3f}")
print(f"–ö–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç –¥–µ—Ç–µ—Ä–º–∏–Ω–∞—Ü–∏–∏ (R^2): {r_value**2:.3f}")
print(f"–ó–Ω–∞—á–µ–Ω–∏–µ p-value: {p_value:.6f}")

# H0: –ú–µ–∂–¥—É –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–º–∏ ùë• –∏ ùë¶ –Ω–µ—Ç –ª–∏–Ω–µ–π–Ω–æ–π –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ (–∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç –∫–æ—Ä—Ä–µ–ª—è—Ü–∏–∏ —Ä–∞–≤–µ–Ω 0).
alpha = 0.05
if p_value < alpha:
    print("–ù—É–ª–µ–≤–∞—è –≥–∏–ø–æ—Ç–µ–∑–∞ H0 –æ—Ç–≤–µ—Ä–≥–∞–µ—Ç—Å—è. –°—É—â–µ—Å—Ç–≤—É–µ—Ç –∑–Ω–∞—á–∏–º–∞—è –ª–∏–Ω–µ–π–Ω–∞—è –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç—å –º–µ–∂–¥—É –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–º–∏ x –∏ y.")
else:
    print("–ù—É–ª–µ–≤–∞—è –≥–∏–ø–æ—Ç–µ–∑–∞ H0 –Ω–µ –æ—Ç–≤–µ—Ä–≥–∞–µ—Ç—Å—è. –ù–µ—Ç –∑–Ω–∞—á–∏–º–æ–π –ª–∏–Ω–µ–π–Ω–æ–π –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –º–µ–∂–¥—É –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–º–∏ x –∏ y.")
